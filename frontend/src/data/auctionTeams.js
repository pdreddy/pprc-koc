export function groupInfoForTeamId(id, fallbackIndex = 0) {
  return AUCTION_GROUPS[id] || { group: fallbackIndex < 8 ? 'A' : 'B', groupOrder: (fallbackIndex % 8) + 1 };
}

export function sortByGroupOrder(a, b) {
  return (a.groupOrder || 0) - (b.groupOrder || 0) || (a.gradient || 0) - (b.gradient || 0);
}

export function normalizeAuctionPlayer(row, index = 0) {
  if (Array.isArray(row)) {
    const [name, tierUtr, actualUtr, basePrice, auctionedMoney, isCaptain] = row;
    return { slot: index + 1, name, tierUtr, actualUtr, basePrice, auctionedMoney, isCaptain: index === 0 || !!isCaptain, utr: actualUtr === '' || actualUtr == null ? '' : actualUtr };
  }
  return {
    slot: row.slot || index + 1,
    name: row.name || row.player || '',
    tierUtr: row.tierUtr ?? row.tierUTR ?? row.tier ?? '',
    actualUtr: row.actualUtr ?? row.actualUTR ?? row.utr ?? '',
    basePrice: row.basePrice ?? '',
    auctionedMoney: row.auctionedMoney ?? row.auctionMoney ?? '',
    isCaptain: index === 0 || !!(row.isCaptain ?? row.captainSlot ?? row.captain),
    utr: row.actualUtr ?? row.actualUTR ?? row.utr ?? ''
  };
}

export function normalizeAuctionTeam(team, index = 0) {
  const idNum = Number(team.id || team.teamId || index + 1);
  const players = (team.players || team.roster || []).map(normalizeAuctionPlayer);
  const captain = players[0]?.name || team.captain || '';
  return {
    id: `team${idNum}`,
    name: team.name,
    abbreviation: team.abbreviation,
    captain,
    totalSpent: team.totalSpent ?? '',
    moneyLeft: team.moneyLeft ?? '',
    players
  };
}

export function buildAuctionTeams() {
  return AUCTION_TEAMS.reduce((teams, team, index) => {
    const normalized = normalizeAuctionTeam(team, index);
    normalized.password = `KOC${normalized.abbreviation}#3`;
    const groupInfo = groupInfoForTeamId(normalized.id, index);
    normalized.gradient = index + 1;
    normalized.group = groupInfo.group;
    normalized.groupOrder = groupInfo.groupOrder;
    teams[normalized.id] = normalized;
    return teams;
  }, {});
}
